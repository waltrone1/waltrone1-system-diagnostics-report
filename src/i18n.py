from __future__ import annotations

import sys
from typing import Any

DEFAULT_LANG = "de"

def get_lang() -> str:
    args = [str(arg).strip() for arg in sys.argv[1:]]
    for index, arg in enumerate(args):
        lower = arg.lower()
        if lower in {"-lang", "/lang", "--lang"} and index + 1 < len(args):
            value = args[index + 1].strip().lower()
            if value in {"de", "en"}:
                return value
        if lower.startswith("-lang=") or lower.startswith("--lang="):
            value = lower.split("=", 1)[1].strip()
            if value in {"de", "en"}:
                return value
    return DEFAULT_LANG

